package slideCalisma;

public class SlideCalisma053 {

	public static void main(String[] args) {
		// Variablenin değerini arttırma.
		
		int x =5;
		x++;
		System.out.println(x);//bir arttırır.
		
		int y = 8;
		y = y+4;
		System.out.println(y);//y yi 4 arttırır.
		
		int z = 8;
		z+=7;
		System.out.println(z);//z yi 7 arttırır.
		
		int t =5;
		t*=3;//t yi 3 ile çarpma(t =t*3 deyapabilirdik)
		System.out.println(t);
		
		int k =35;
		k-=18;
		System.out.println(k);//k yı 18 azalttık.
		
		int m = 45;
		m--;
		System.out.println(m);// m yi bir azalttık.

		int n =45;
		n/=5;// n=n/5 de yapabilirdik.
		System.out.println(n);
		
	}

}
